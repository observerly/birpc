/*****************************************************************************************************************/

type server struct {
	pb.UnimplementedStorageServiceHandler
	App *firebase.App
}

/*****************************************************************************************************************/

func NewStorageServer(app *firebase.App) *server {
	return &server{
		App: app,
	}
}

